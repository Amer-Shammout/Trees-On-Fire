    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        String data = dataTF.getText();
        String children = childrenTF.getText();
        int childrenNum = Integer.parseInt(childrenNumTF.getText());
        if(children.length() != childrenNum+(childrenNum-1))
        {
            JOptionPane.showMessageDialog(null, "The children input is wrong!");
        }
        else
        {
            Node n = new Node(data.charAt(0));
            List<String> l = new ArrayList<>();
            l = Arrays.asList(children.split(" "));
            GeneralTree.insert(n, l);
        }
        
        dataTF.setText("");
        childrenTF.setText("");
        childrenNumTF.setText("");
    }//GEN-LAST:event_jButton8ActionPerformed

